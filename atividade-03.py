number=int(input("Digite um número: " ))  # pede ao usuário um número 

if number % 2 == 0: # se o número nao tiver resto na divisão 
    print('O número:',number, "é par") # imprime na tela que o número é par
else:
    print('O número:',number, "é ímpar") # caso tenha,  imprime que o número é ímpar. 

