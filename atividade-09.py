n = int(input("Digite um núemro: ")) 

number = 1 #definimos a variável como 1
           # pois em fatorial a multiplicação termina em 1. 

for n in range(1, n+1 ):  # gerará um número no intervalo de 1 a n+1 
    number*= n  # number multiplicado a cada número no intervalo

print(f'{n} fatorial é: {number}') # imprime o número na tela 






    








